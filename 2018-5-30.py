#全等
result = "success"

if result == "success":
	print("test pass")
	
else:
	print("test fail")
	
#in or  not in	
a = "hello"
b = "hello world"

if a in b:
	print("Contain")
else:
	print("Not contain")
	
#bool	
c = False
if c:
	print("c is true")
else:
	print("c is false")
	
	
#多重判断
d = 72

if d >= 90:
	print("优秀")
elif d >= 70:
	print("良好")
elif d >= 60:
	print("及格")
else:
	print("不及格")

	
